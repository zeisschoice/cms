package com.zeiss.cms.model;

public class MonCost {

	 private Integer month;
	 private Integer year;
	 private Double water;
	 private Double electric;
	 private Double internet;
	 private Double gas;
	 private Double tv;
	 private Double equally;
	 private Double other;
	 private Double manage;
	 
	 
	
	public Double getManage() {
		return manage;
	}
	public void setManage(Double manage) {
		this.manage = manage;
	}
	public Integer getMonth() {
		return month;
	}
	public void setMonth(Integer month) {
		this.month = month;
	}
	public Integer getYear() {
		return year;
	}
	public void setYear(Integer year) {
		this.year = year;
	}
	public Double getWater() {
		return water;
	}
	public void setWater(Double water) {
		this.water = water;
	}
	public Double getElectric() {
		return electric;
	}
	public void setElectric(Double electric) {
		this.electric = electric;
	}
	public Double getInternet() {
		return internet;
	}
	public void setInternet(Double internet) {
		this.internet = internet;
	}
	public Double getGas() {
		return gas;
	}
	public void setGas(Double gas) {
		this.gas = gas;
	}
	public Double getTv() {
		return tv;
	}
	public void setTv(Double tv) {
		this.tv = tv;
	}
	public Double getEqually() {
		return equally;
	}
	public void setEqually(Double equally) {
		this.equally = equally;
	}
	public Double getOther() {
		return other;
	}
	public void setOther(Double other) {
		this.other = other;
	}
	
	
}
